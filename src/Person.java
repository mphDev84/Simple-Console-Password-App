
public class Person {
	
	String name;
	String info;
	int age;


	public Person(String name) {
		this.name = name;
		this.info = info;
		this.age = age;
	}

	public String getInfo() {
		return info;
	}

	public int getAge() {
		return age;
	}
	
	public void  setName(String name) {
		this.name = name;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public void setAge(int age) {
		this.age = age;
	}
}

